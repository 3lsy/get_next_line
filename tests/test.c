#include "get_next_line.h"
#include <stdio.h>
#include <fcntl.h>

void	reading(int fd)
{
	char	*line;

	line = get_next_line(fd);
	while (line)
	{
		printf("%s", line);
		free(line);
		line = get_next_line(fd);
	}
	if (line)
		free(line);
}

int	main(int argc, char **argv)
{
	int		fd;

	if (argc == 1)
		fd = 0;
	else if (argc == 2)
		fd = open(argv[1], O_RDONLY);
	else
		return (2);
	reading(fd);
	if (argc == 2)
		close(fd);
}
